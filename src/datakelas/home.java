    private void selectData() {
        String kolom[] = {"NIS","Nama","Absen","Tempat Lahir","Tanggal Lahir","Alamat"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_data";
        ResultSet rs = Koneksi_DB.executeQuery(SQL);
        try {
            while(rs.next()){
                String nis = rs.getString(1);
                String nama = rs.getString(2);
                String absen = rs.getString(3);
                String tempat_lahir = rs.getString(4);
                String tanggal_lahir = rs.getString(5);
                String alamat = rs.getString(6);
                String data[] = {nis, nama, absen, tempat_lahir, tanggal_lahir, alamat};
                dtm.addRow(data);
            }
        } catch(SQLException ex){
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbData.setModel(dtm);
    }
