    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(dcTanggal.getDate());

                
        if ("".equals(tfNIS.getText())||"".equals(tfNama.getText())||"".equals(tfAbsen.getText())||"".equals(tfTempat.getText())||"".equals(tanggal)||"".equals(tfAlamat.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
         
         String SQL = "INSERT INTO tb_data(nis, nama, absen, tempat_lahir, tanggal_lahir, alamat) " + "VALUES('"+tfNIS.getText()+"','"+tfNama.getText()+"','"+tfAbsen.getText()+"','"+tfTempat.getText()+"','"+tanggal+"','"+tfAlamat.getText()+"')";
         int status = Koneksi_DB.execute(SQL);
         if (status == 1){
             JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
             selectData();
         }
         else{
             JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
         }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        int baris = tbData.getSelectedRow();
        if(baris!= -1){
          tfNIS.setText(tbData.getValueAt(baris, 0).toString());
          tfNama.setText(tbData.getValueAt(baris, 1).toString());
          tfAbsen.setText(tbData.getValueAt(baris, 2).toString());
          tfTempat.setText(tbData.getValueAt(baris, 3).toString());
          tfAlamat.setText(tbData.getValueAt(baris, 5).toString());
          
          
          SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try {
            dateFormat = date.parse(tbData.getValueAt(baris, 4).toString());
            } catch (ParseException ex) {
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
            }

            dcTanggal.setDate(dateFormat);            
      } 
    }//GEN-LAST:event_tbDataMouseClicked

